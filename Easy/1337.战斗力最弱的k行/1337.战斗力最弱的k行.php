class Solution {
    
    /**
     * @param Integer[][] $mat
     * @param Integer $k
     * @return Integer[]
     */
    function kWeakestRows($mat, $k) {
        
        $map = [];
        foreach ($mat as $line => $mem) {
            $count1 = array_count_values($mem);
            $map['key'][] = $line;
            $map['value'][] = $count1[1] ?? 0;
        }
        
        array_multisort($map['value'], SORT_ASC, $map['key'], SORT_ASC);
        $res = array_slice($map['key'], 0, $k);
        return $res;
        
    }
}

