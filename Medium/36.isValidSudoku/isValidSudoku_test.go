package _6_isValidSudoku

import (
	"fmt"
	"testing"
)

func TestIsValidSudoku(t *testing.T)  {
	tests := []struct{
		name string
		args [][]string
		want bool
	}{
		{
			name: "test1",
			args: [][]string{
				{"5","3",".",".","7",".",".",".","."},
				{"6",".",".","1","9","5",".",".","."},
				{".","9","8",".",".",".",".","6","."},
				{"8",".",".",".","6",".",".",".","3"},
				{"4",".",".","8",".","3",".",".","1"},
				{"7",".",".",".","2",".",".",".","6"},
				{".","6",".",".",".",".","2","8","."},
				{".",".",".","4","1","9",".",".","5"},
				{".",".",".",".","8",".",".","7","9"},
			},
			want: true,
		},
		{
			name: "test2",
			args: [][]string{
				{"8","3",".",".","7",".",".",".","."},
				{"6",".",".","1","9","5",".",".","."},
				{".","9","8",".",".",".",".","6","."},
				{"8",".",".",".","6",".",".",".","3"},
				{"4",".",".","8",".","3",".",".","1"},
				{"7",".",".",".","2",".",".",".","6"},
				{".","6",".",".",".",".","2","8","."},
				{".",".",".","4","1","9",".",".","5"},
				{".",".",".",".","8",".",".","7","9"},
			},
			want: false,
		},
	}

	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			res := isValidSudoku(test.args)
			fmt.Printf(" %v\n ", res)
			if res != test.want {
				t.Errorf("isValidSudoku = %v want %v", res, test.want)
			}
		})
	}
}